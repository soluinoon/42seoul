int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (s1[i] != '\0' && s2[i] != '\0' && i < n)
	{
		if (s1[i] != s2[i])
		{
			if (s1[i] > s2[i])
				return (s1 - s2);
			else
				return (s2 - s1);
		}
		i++;
	}
	return (0);
}
