#include "libft.h"

void	*ft_memchr(const void *str, int c, size_t n)
{
	unsigned char	*str_new;

	str_new = (unsigned char *)str;
	while (n--)
	{
		if (*str_new == (unsigned char)c)
			return (str_new);
		str_new++;
	}
	return (NULL);
}
